function maiorOuIgualNumero(numero1, numero2){
    if (numero1 >= numero2) {
        console.log(`O numero ${numero1} é maior ou igual o numero ${numero2}`)
    } else {
        console.log(`O numero ${numero2} é maior ou igual o numero ${numero1}`)
    }
}

maiorOuIgualNumero(0,0)
maiorOuIgualNumero(0,"0");
